package uk.ac.sussex.midasAnalyser.feature;

public class Stimulus {

	private String id;
	private StimulusType type;
	
	public Stimulus() {

	}
	
	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public StimulusType getType() {
		return type;
	}

	public void setType(StimulusType type) {
		this.type = type;
	}
	
	
	
}
